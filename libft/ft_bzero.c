#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ch_s_ptr;

	ch_s_ptr = (char *)s;
	i = 0;
	while (i < n)
	{
		ch_s_ptr[i] = '\0';
		i++;
	}
}
