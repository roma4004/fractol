#include "libft.h"

void	ft_strclr(char *s)
{
	unsigned int	i;
	unsigned int	len;

	i = 0;
	len = 0;
	if (s)
	{
		while (s[len])
			len++;
		while (i < len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
